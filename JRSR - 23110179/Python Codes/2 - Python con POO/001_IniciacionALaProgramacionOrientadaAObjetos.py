# --- Introducción a las Clases y Objetos ---

# Una clase es una plantilla para crear objetos. Define un conjunto de atributos y métodos.
class NombreClase:
    # 'pass' es una instrucción nula. Se usa cuando se requiere una declaración por sintaxis pero no se quiere ejecutar ningún código.
    pass

# La creación de un objeto a partir de una clase se llama 'instanciación'.
# 'primerObjeto' es ahora una instancia de 'NombreClase'.
primerObjeto = NombreClase()

# Imprimimos el objeto para ver su representación en memoria.
print(primerObjeto)